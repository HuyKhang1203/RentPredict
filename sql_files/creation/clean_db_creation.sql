CREATE DATABASE IF NOT EXISTS CLEAN;
USE CLEAN;

CREATE TABLE MUMBAI(
SELLER_TYPE VARCHAR(10), 
BEDROOM FLOAT, 
LAYOUT VARCHAR(3), 
PROPERTY_TYPE VARCHAR(20), 
LOCALITY VARCHAR(100), 
PRICE FLOAT, 
AREA FLOAT, 
FURNISH_TYPE VARCHAR(20), 
BATHROOM FLOAT
);


CREATE TABLE DELHI LIKE MUMBAI;
CREATE TABLE KOLKATA LIKE MUMBAI;
CREATE TABLE BANGALORE LIKE MUMBAI;
CREATE TABLE HYDERABAD LIKE MUMBAI;
CREATE TABLE CHENNAI LIKE MUMBAI;
CREATE TABLE AHEMDABAD LIKE MUMBAI;
CREATE TABLE PUNE LIKE MUMBAI;